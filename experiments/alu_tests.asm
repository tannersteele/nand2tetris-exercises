// CLEAR RAM BEFORE RUNNING, WILL SET VALUES AS REQUIRED
// DEMONSTRATES ADDING + SUBTRACTING + LOOPING

(BEGIN)
    @5     //SET CONSTANT 5
    D=A    //CURRENT DATA INSTRUCTION = VALUE OF REGISTER ABOVE
    @R0    //INITIALIZE R0 TO 0
    M=0
    @R1    //SET R1 to CONSTANT 5
    M=D
(LOOP)     //LOOP UNTIL VALUE OF R1 == R0
    @R0
    M=M+1  //SET R0 TO CURRENT VALUE + 1
    @R1
    M=M-1  //SET R1 TO CURRENT VALUE -1
    D=M    //PASS CONTENTS OF R1 TO D REGISTER
    @END
    D;JEQ  //IF D == 0 JUMP TO END
    @LOOP
    0;JMP
(END)
    @END
    0;JMP